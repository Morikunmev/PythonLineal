#Realizar la carga de dos numeros enteros por teclado e imprimir su suma y su producto

num1 = input("Ingresa un numero: ")
num2 = input("Ingresa otro numero: ")
num1 = int(num1)
num2 = int(num2)

suma = num1 + num2
producto = num1 * num2

print("La suma de los dos numeros es: " + str(suma))
print("El producto de los dos numeros es: " + str(producto))

